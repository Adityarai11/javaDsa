public class javabasic11 {
    public static void main(String arg[]){
        System.out.println((3<2)&&(4>5)) ;/*logic operator(logical AND) */
        System.out.println((1<2)&&(6<1));
        System.out.println((2<3)&&(1<4));
        System.out.println((3>4)||(1>2));/*logic OR */    
    }
    
}
